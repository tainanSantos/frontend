export class SpriteDirection {

  up: HTMLImageElement;
  left: HTMLImageElement;
  right: HTMLImageElement;
  down: HTMLImageElement;


  constructor(up: HTMLImageElement, left: HTMLImageElement, right: HTMLImageElement, down: HTMLImageElement) {
    this.up = up;
    this.left = left;
    this.right = right;
    this.down = down;
  }
}
